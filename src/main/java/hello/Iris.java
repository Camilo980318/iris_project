package hello;


public class Iris {


	public double sl;
	public double sw;
	public double pl;
	public double pw;


	// Constructors

	public Iris( double sl, double sw, double pl, double pw) {
			

		this.sl = sl;
		this.sw = sw;
		this.pl = pl;
		this.pw = pw;
	
	}
	

	public double getSl() {
		return sl;
	}

	public void setSl(double sl2) {
		this.sl = sl2;
	}


	
	public double getSw() {
		return sw;
	}
	
	public void setSw(double sw) {
		this.sw = sw;
	}


	
	public double getPl() {
		return pl;
	}

	public void setPl(double pl) {
		this.pl = pl;
	}

	

	public double getPw() {
		return pw;
	}

	public void setPw(double pw) {
		this.pw = pw;
	}
	
	

	
}
